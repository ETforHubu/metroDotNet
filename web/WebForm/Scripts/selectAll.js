function checkAll() {
    var checklist = document.getElementsByTagName("input");
    var dels = document.getElementById("dels");
    for (var i = 0; i < checklist.length; i++) {
        if (checklist[i].type == "checkbox") {
            checklist[i].checked = dels.checked;
        }
    }
}
